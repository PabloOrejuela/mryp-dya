            <p id="txt-descripcion">El Centro de Desarrollo y Autogestión desarrolla esta iniciativa como parte del Programa Multianual de Resiliencia para la inclusión educativa de niños, niñas y adolescentes en situación de movilidad humana, vulnerabilidad y otras situaciones de emergencia (MYRP), impulsado por UNESCO.
Cuenta con el apoyo financiero de Unicef, Embajada de Canadá en Ecuador, Education Cannot Wait y Unión Europea Ayuda Humanitaria. 

El programa busca mejorar la calidad educativa y facilitar el acceso educativo a población en condición de movilidad humana y población de acogida.

Se implementa en 7 provincias del país: Azuay, Pichincha, Imbabura, Carchi, Guayas, El Oro y Manabí.</p>
